import { createReducer, createActions } from 'reduxsauce'
import Immutable from 'seamless-immutable'
/* ------------- Types and Action Creators ------------- */
/**
 * Inialisation des 3 actions
 */

const { Types, Creators } = createActions({
    getRapportContenuRequest: ['response'],
    getRapportContenuSuccess: ['response', 'loading'],
    getRapportContenuFailure: ['error'],
})

export const getRapportContenuTypes = Types
export default Creators

/* ------------- Initial State ------------- */

export const INITIAL_STATE = Immutable({
    response: null,
    error: null,
    loading: null,
})

/* ------------- Reducers ------------- */
/**
 * reducers action request
 * @param {*} state
 */
const getRapportContenuSuccess = (state, { response }) =>
    state.merge({
        error: false,
        response,
        loading: false,
    })
/**
 * reducers action failure
 * @param {*} state
 */
const getRapportContenuFailure = (state, { error }) =>
    state.merge({
        loading: false,
        error,
    })
/**
 * reducers action request
 * @param {*} state
 */
const getRapportContenuRequest = state =>
    state.merge({ loading: true, error: null })

/**
 * render redux reducer actions
 */
export const reducer = createReducer(INITIAL_STATE, {
    [Types.GET_RAPPORT_CONTENU_REQUEST]: getRapportContenuRequest,
    [Types.GET_RAPPORT_CONTENU_SUCCESS]: getRapportContenuSuccess,
    [Types.GET_RAPPORT_CONTENU_FAILURE]: getRapportContenuFailure,
})
