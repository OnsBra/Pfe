import { createReducer, createActions } from 'reduxsauce'
import Immutable from 'seamless-immutable'
/* ------------- Types and Action Creators ------------- */
/**
 * Inialisation des 3 actions
 */

const { Types, Creators } = createActions({
    deleteActualiteRequest: ['response'],
    deleteActualiteSuccess: ['response', 'loading'],
    deleteActualiteFailure: ['error'],
})

export const deleteActualiteTypes = Types
export default Creators

/* ------------- Initial State ------------- */

export const INITIAL_STATE = Immutable({
    response: null,
    error: null,
    loading: null,
})

/* ------------- Reducers ------------- */
/**
 * reducers action request
 * @param {*} state
 */
const deleteActualiteSuccess = (state, { response }) =>
    state.merge({
        error: false,
        response,
        loading: false,
    })
/**
 * reducers action failure
 * @param {*} state
 */
const deleteActualiteFailure = (state, { error }) =>
    state.merge({
        loading: false,
        error,
    })
/**
 * reducers action request
 * @param {*} state
 */
const deleteActualiteRequest = state =>
    state.merge({ loading: true, error: null })

/**
 * render redux reducer actions
 */
export const reducer = createReducer(INITIAL_STATE, {
    [Types.DELETE_ACTUALITE_REQUEST]: deleteActualiteRequest,
    [Types.DELETE_ACTUALITE_SUCCESS]: deleteActualiteSuccess,
    [Types.DELETE_ACTUALITE_FAILURE]: deleteActualiteFailure,
})
