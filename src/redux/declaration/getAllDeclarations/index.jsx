import { createReducer, createActions } from 'reduxsauce'
import Immutable from 'seamless-immutable'
/* ------------- Types and Action Creators ------------- */
/**
 * Inialisation des 3 actions
 */

const { Types, Creators } = createActions({
    getAllDeclarationsRequest: ['response'],
    getAllDeclarationsSuccess: ['response', 'loading'],
    getAllDeclarationsFailure: ['error'],
})

export const getAllDeclarationsTypes = Types
export default Creators

/* ------------- Initial State ------------- */

export const INITIAL_STATE = Immutable({
    response: null,
    error: null,
    loading: null,
})

/* ------------- Reducers ------------- */
/**
 * reducers action request
 * @param {*} state
 */
const getAllDeclarationsSuccess = (state, { response }) =>
    state.merge({
        error: false,
        response,
        loading: false,
    })
/**
 * reducers action failure
 * @param {*} state
 */
const getAllDeclarationsFailure = (state, { error }) =>
    state.merge({
        loading: false,
        error,
    })
/**
 * reducers action request
 * @param {*} state
 */
const getAllDeclarationsRequest = state =>
    state.merge({ loading: true, error: null })

/**
 * render redux reducer actions
 */
export const reducer = createReducer(INITIAL_STATE, {
    [Types.GET_ALL_DECLARATIONS_REQUEST]: getAllDeclarationsRequest,
    [Types.GET_ALL_DECLARATIONS_SUCCESS]: getAllDeclarationsSuccess,
    [Types.GET_ALL_DECLARATIONS_FAILURE]: getAllDeclarationsFailure,
})
