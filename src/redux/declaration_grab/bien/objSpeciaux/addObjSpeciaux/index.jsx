import { createReducer, createActions } from 'reduxsauce'
import Immutable from 'seamless-immutable'
/* ------------- Types and Action Creators ------------- */
/**
 * Inialisation des 3 actions
 */

const { Types, Creators } = createActions({
    addObjSpeciauxRequest: ['response'],
    addObjSpeciauxSuccess: ['response', 'loading'],
    addObjSpeciauxFailure: ['error'],
})

export const addObjSpeciauxTypes = Types
export default Creators

/* ------------- Initial State ------------- */

export const INITIAL_STATE = Immutable({
    response: null,
    error: null,
    loading: null,
})

/* ------------- Reducers ------------- */

/**
 * reducers action success
 * @param {*} state
 */
const addObjSpeciauxSuccess = (state, { response }) =>
    state.merge({
        error: false,
        response,
        loading: false,
    })
/**
 * reducers action failure
 * @param {*} state
 */
const addObjSpeciauxFailure = (state, { error }) => {
    const { response } = error
    return state.merge({
        loading: false,
        error: true,
        response,
    })
}
/**
 * reducers action request
 * @param {*} state
 */
const addObjSpeciauxRequest = state =>
    state.merge({ loading: true, error: null })

/**
 * render redux reducer actions
 */
export const reducer = createReducer(INITIAL_STATE, {
    [Types.ADD_OBJ_SPECIAUX_REQUEST]: addObjSpeciauxRequest,
    [Types.ADD_OBJ_SPECIAUX_SUCCESS]: addObjSpeciauxSuccess,
    [Types.ADD_OBJ_SPECIAUX_FAILURE]: addObjSpeciauxFailure,
})
