import { createReducer, createActions } from 'reduxsauce'
import Immutable from 'seamless-immutable'

/* ------------- Types and Action Creators ------------- */
/**
 * Inialisation des 3 actions
 */

const { Types, Creators } = createActions({
    updateSpecesRequest: ['response'],
    updateSpecesSuccess: ['response', 'loading'],
    updateSpecesFailure: ['error'],
})

export const updateSpecesTypes = Types
export default Creators

/* ------------- Initial State ------------- */

export const INITIAL_STATE = Immutable({
    response: null,
    error: null,
    loading: null,
})

/* ------------- Reducers ------------- */
/**
 * reducers action request
 * @param {*} state
 */

/**
 * reducers action request
 * @param {*} state
 */
const updateSpecesRequest = state => state.merge({ loading: true })

/**
 * reducers action request
 * @param {*} state
 */
const updateSpecesSuccess = (state, { response }) =>
    state.merge({
        error: false,
        response,
    })

/**
 * reducers action request
 * @param {*} state
 */
const updateSpecesFailure = (state, { error }) => {
    const { response } = error
    return state.merge({
        loading: false,
        error: true,
        response,
    })
}

/**
 * render redux reducer actions
 */
export const reducer = createReducer(INITIAL_STATE, {
    [Types.UPDATE_SPECES_REQUEST]: updateSpecesRequest,
    [Types.UPDATE_SPECES_SUCCESS]: updateSpecesSuccess,
    [Types.UPDATE_SPECES_FAILURE]: updateSpecesFailure,
})
